package com.cou_det.model;

public class CouDetVO {
	private String cou_id;
	private String mov_id;
	private Integer mov_order;
	private Integer mov_play_time;
	
	public String getCou_id() {
		return cou_id;
	}
	public void setCou_id(String cou_id) {
		this.cou_id = cou_id;
	}
	public String getMov_id() {
		return mov_id;
	}
	public void setMov_id(String mov_id) {
		this.mov_id = mov_id;
	}
	public Integer getMov_order() {
		return mov_order;
	}
	public void setMov_order(Integer mov_order) {
		this.mov_order = mov_order;
	}
	public Integer getMov_play_time() {
		return mov_play_time;
	}
	public void setMov_play_time(Integer mov_play_time) {
		this.mov_play_time = mov_play_time;
	}
	
	
}
