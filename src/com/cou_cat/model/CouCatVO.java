package com.cou_cat.model;

public class CouCatVO {
	private String cou_cat_id;
	private String cou_cat_name;
	private String cou_cat_info;
	private byte[] cou_cat_img;
	
	public String getCou_cat_id() {
		return cou_cat_id;
	}
	public void setCou_cat_id(String cou_cat_id) {
		this.cou_cat_id = cou_cat_id;
	}
	public String getCou_cat_name() {
		return cou_cat_name;
	}
	public void setCou_cat_name(String cou_cat_name) {
		this.cou_cat_name = cou_cat_name;
	}
	public String getCou_cat_info() {
		return cou_cat_info;
	}
	public void setCou_cat_info(String cou_cat_info) {
		this.cou_cat_info = cou_cat_info;
	}
	public byte[] getCou_cat_img() {
		return cou_cat_img;
	}
	public void setCou_cat_img(byte[] cou_cat_img) {
		this.cou_cat_img = cou_cat_img;
	}
	
	
}
